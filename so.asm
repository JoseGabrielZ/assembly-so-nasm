org 0x7C00 ; ORGANISA O OFFSET

; SETANDO O MODO DE VIDEO
MOV AH, 0x00
MOV AL, 0x13
INT 0x10

; PARAMETROS
MOV AL, 0x01 ; COR DO PIXEL
MOV BH, 0x00 ; PAGINA
MOV CX, 0x00 ; X
MOV DX, 0x00 ; Y

LOOP:
	MOV AH, 0x0C
	INT 0x10
	
	INC CX
	CMP CX, 0x140
	JNE LOOP
	
	MOV CX, 0x00
	
	INC DX
	CMP DX, 0xC8
	JNE LOOP
	
	MOV DX, 0x00
	
	INC AL
	CMP AL, 0xFF
	JNE LOOP
	
	MOV AL, 0x00
	
	JMP LOOP
TIMES 510-($-$$) DB 0

; GRAVA OS BYTES NA POSIÇÃO CORRETA, PARA SER RECONHECIDO COMO UMA ISO BOOTAVEL
DB 0x55
DB 0xAA